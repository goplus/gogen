package astutil

import (
	"bytes"
	"fmt"

	"github.com/goplus/gogen/target/js"
)

// ExprString returns the (possibly shortened) string representation for x.
// Shortened representations are suitable for user interfaces but may not
// necessarily follow JavaScript syntax.
func ExprString(x js.Expr) string {
	var buf bytes.Buffer
	WriteExpr(&buf, x)
	return buf.String()
}

// WriteExpr writes the (possibly shortened) string representation for x to buf.
// Shortened representations are suitable for user interfaces but may not
// necessarily follow JavaScript syntax.
func WriteExpr(buf *bytes.Buffer, x js.Expr) {
	// The AST preserves source-level parentheses so there is
	// no need to introduce them here to correct for different
	// operator precedences. (This assumes that the AST was
	// generated by a JavaScript parser.)

	switch x := x.(type) {
	default:
		fmt.Fprintf(buf, "(ast: %T)", x) // nil, ast.BadExpr, ast.KeyValueExpr

	case *js.Ident:
		buf.WriteString(x.Name)

	case *js.BasicLit:
		buf.WriteString(x.Value)

	case *js.ArrayLit:
		buf.WriteByte('[')
		writeExprList(buf, x.Values)
		buf.WriteByte(']')

	case *js.FuncLit:
		buf.WriteByte('(')
		writeIdentList(buf, x.Params)
		buf.WriteString(" literal)") // shortened

	case *js.ParenExpr:
		buf.WriteByte('(')
		WriteExpr(buf, x.X)
		buf.WriteByte(')')

	case *js.SelectorExpr:
		WriteExpr(buf, x.X)
		buf.WriteByte('.')
		buf.WriteString(x.Sel.Name)

	case *js.IndexExpr:
		WriteExpr(buf, x.X)
		buf.WriteByte('[')
		WriteExpr(buf, x.Index)
		buf.WriteByte(']')

	case *js.CallExpr:
		WriteExpr(buf, x.Fun)
		buf.WriteByte('(')
		writeExprList(buf, x.Args)
		if hasDots(x) {
			buf.WriteString("...")
		}
		buf.WriteByte(')')

	case *js.UnaryExpr:
		buf.WriteString(x.Op.String())
		WriteExpr(buf, x.X)

	case *js.BinaryExpr:
		WriteExpr(buf, x.X)
		buf.WriteByte(' ')
		buf.WriteString(x.Op.String())
		buf.WriteByte(' ')
		WriteExpr(buf, x.Y)
	}
}

func writeIdentList(buf *bytes.Buffer, list []*js.Ident) {
	for i, x := range list {
		if i > 0 {
			buf.WriteString(", ")
		}
		buf.WriteString(x.Name)
	}
}

func writeExprList(buf *bytes.Buffer, list []js.Expr) {
	for i, x := range list {
		if i > 0 {
			buf.WriteString(", ")
		}
		WriteExpr(buf, x)
	}
}

func hasDots(call *js.CallExpr) bool { return call.Ellipsis.IsValid() }
